/**
 * Применить язык ко всей странице
 * @param {string} lang — 'ru' или 'kg'
 */
function setLang(lang) {
    if (!T[lang]) return;
    LANG = lang;

    // Сохранить выбор
    try { localStorage.setItem('aknet_lang', lang); } catch(e) {}

    // Обновить кнопки переключателя (в шапке и мобильном меню)
    document.querySelectorAll('.lang-sw button').forEach(function(btn) {
        var isActive = btn.getAttribute('data-lang') === lang;
        btn.classList.toggle('active', isActive);
    });

    // Перевести все элементы с data-i18n
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
        var key = el.getAttribute('data-i18n');
        if (T[lang][key] !== undefined) {
            el.innerHTML = T[lang][key];
        }
    });

    // Перевести placeholder-ы
    document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
        var key = el.getAttribute('data-i18n-ph');
        if (T[lang][key] !== undefined) {
            el.placeholder = T[lang][key];
        }
    });

    // Обновить lang у <html>
    document.documentElement.lang = lang === 'kg' ? 'ky' : 'ru';
}
